/*
                                DIFERENCIA CON JAVA 
Java y JavaScript son dos lenguajes de programación diferentes que, aunque tienen nombres similares, 
tienen diferentes usos y propósitos.

Java es un lenguaje de programación orientado a objetos que se usa principalmente para crear aplicaciones 
de escritorio, móviles, sistemas embebidos, juegos (como minecraft) y aplicaciones empresariales. 

Java es un lenguaje compilado, 
osea que el código fuente se compila en un archivo ejecutable antes de su uso.


JavaScript, por otro lado, es un lenguaje de programación interpretado que se usa principalmente 
para crear aplicaciones web y en el lado del cliente (front-end). 
se ejecuta en el navegador web del usuario y se utiliza para crear interacciones dinámicas, 
efectos visuales, formularios interactivos y muchas otras funciones mas.

En resumen, Java es más adecuado para crear aplicaciones de escritorio, móviles y empresariales, 
y JavaScript se usa principalmente para crear aplicaciones web y en el lado del cliente. */


